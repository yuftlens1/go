package main

import "fmt"

func main() {
	var defaultInt int         //默认值是0
	var defaultFloat32 float32 //默认值是0.000
	var defaultFloat64 float64 //默认值是0.000
	var defaultBool bool       //默认值是false
	var defaultString string   //空的
	fmt.Println(defaultInt, defaultBool, defaultFloat32, defaultFloat64, defaultString)
}
